import userModel from "../models/user.model.js";
import courseModel from "../models/course.model.js"; 

// 1. GET ALL STUDENTS REGISTRATION PIPELINE
export async function getAllStudents(req, res) {
  try {
    // Zero dependencies clear query execution
    const studentsList = await userModel.find({ isAdmin: false });
    
    return res.status(200).json({ 
      success: true, 
      students: studentsList || [] 
    });
  } catch (err) {
    return res.status(500).json({ 
      success: false, 
      message: "Direct Controller Crash Trace: " + err.message 
    });
  }
}

// 2. APPROVE OR REJECT STUDENT PROFILE WITH AUTOGENERATED STUDENT ID
export async function updateStudentStatus(req, res) {
  try {
    // ✅ FIXED: Fallback checking handles both parameter naming formats smoothly
    const studentIdToUpdate = req.params.studentIdToUpdate || req.params.studentId;
    const { status } = req.body; 

    if (!["Approved", "Rejected"].includes(status)) {
      return res.status(400).json({ success: false, message: "Invalid status state transition." });
    }

    const updateFields = { registrationStatus: status };

    if (status === "Approved") {
      updateFields.isVerified = true;
      
      const existingUser = await userModel.findById(studentIdToUpdate);
      if (existingUser && !existingUser.studentId) {
        const timestampToken = Date.now().toString().slice(-4);
        const randomHex = Math.floor(100 + Math.random() * 900); 
        updateFields.studentId = `INT-${timestampToken}${randomHex}`;
      }
    } else if (status === "Rejected") {
      updateFields.isVerified = false;
    }

    const updatedStudent = await userModel.findByIdAndUpdate(
      studentIdToUpdate,
      { $set: updateFields },
      { new: true }
    ).select("-password");

    if (!updatedStudent) {
      return res.status(404).json({ success: false, message: "Student account context target not found." });
    }

    return res.status(200).json({ 
      success: true, 
      message: `Account has been successfully ${status}`, 
      student: updatedStudent 
    });
  } catch (err) {
    return res.status(500).json({ success: false, message: "Status Engine Mutation Crash: " + err.message });
  }
}

// 3. VIEW ACTIVE ENROLLMENTS MATRIX
export async function getCourseEnrollmentMatrix(req, res) {
  try {
    const enrollmentMatrix = await userModel.aggregate([
      { $match: { isAdmin: false, isEnrolled: true } }, 
      { $unwind: "$enrolledCourses" }, 
      {
        $group: {
          _id: "$enrolledCourses", 
          studentsCount: { $sum: 1 },
          students: {
            $push: {
              _id: "$_id",
              fullName: "$fullName",
              emailAddress: "$emailAddress",
              department: "$department",
              universityRoll: "$universityRoll"
            }
          }
        }
      },
      {
        $lookup: {
          from: "courses", 
          localField: "_id",
          foreignField: "_id",
          as: "courseDetails"
        }
      },
      { $unwind: { path: "$courseDetails", preserveNullAndEmptyArrays: true } } // ✅ FIXED: Fallback prevent crash on empty arrays
    ]);

    return res.status(200).json({ success: true, matrix: enrollmentMatrix || [] });
  } catch (err) {
    return res.status(500).json({ success: false, message: "Matrix Aggregation Crash: " + err.message });
  }
}

// 4. TOGGLE LIVE CLASS CHAT CHANNEL
export async function toggleCourseChat(req, res) {
  try {
    const courseId = req.params.courseId || req.params.id;
    const { isChatEnabled } = req.body; 

    if (typeof isChatEnabled !== "boolean") {
      return res.status(400).json({ success: false, message: "isChatEnabled status flag must be a boolean value." });
    }

    const updatedCourse = await courseModel.findByIdAndUpdate(
      courseId,
      { $set: { isChatEnabled } },
      { new: true }
    );

    if (!updatedCourse) {
      return res.status(404).json({ success: false, message: "Target course instance registry not found." });
    }

    return res.status(200).json({
      success: true,
      message: `Live classroom chat has been successfully ${isChatEnabled ? 'enabled' : 'disabled'}.`,
      course: updatedCourse
    });
  } catch (err) {
    return res.status(500).json({ success: false, message: "Chat Mutation Pipeline Error: " + err.message });
  }
}

// 5. PERMANENT COURSE PURGE DELETION
export async function deleteCourse(req, res) {
  try {
    const courseId = req.params.courseId || req.params.id;

    const deletedCourse = await courseModel.findByIdAndDelete(courseId);

    if (!deletedCourse) {
      return res.status(404).json({ success: false, message: "Target course model registry not found." });
    }

    return res.status(200).json({ 
      success: true, 
      message: `Course "${deletedCourse.courseName}" has been successfully removed from the system registry.` 
    });
  } catch (err) {
    return res.status(500).json({ success: false, message: "Deletion Drop Failure: " + err.message });
  }
}