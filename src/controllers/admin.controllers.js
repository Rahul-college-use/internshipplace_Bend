import userModel from "../models/user.model.js";

// 1. GET ALL STUDENTS REGISTRATION PIPELINE (For Admin Grid View)
export async function getAllStudents(req, res) {
  try {
    // Saare non-admin users ko fetch karein aur newest register entries ko pehle dikhayein
    const students = await userModel.find({ isAdmin: false })
      .select("-password")
      .sort({ createdAt: -1 });

    return res.status(200).json({ success: true, students });
  } catch (err) {
    return res.status(500).json({ success: false, message: "Admin Fetch Crash: " + err.message });
  }
}

// 2. APPROVE OR REJECT STUDENT PROFILE WITH AUTOGENERATED STUDENT ID
export async function updateStudentStatus(req, res) {
  try {
    const { studentIdToUpdate } = req.params;
    const { status } = req.body; // Expects "Approved" or "Rejected"

    if (!["Approved", "Rejected"].includes(status)) {
      return res.status(400).json({ success: false, message: "Invalid status state transition." });
    }

    const updateFields = { registrationStatus: status };

    if (status === "Approved") {
      updateFields.isVerified = true;
      
      // Check karein agar student ke paas pehle se allocated studentId nahi hai
      const existingUser = await userModel.findById(studentIdToUpdate);
      if (!existingUser.studentId) {
        // Dynamic Unique Generation Rule (e.g., INT2026CSE001 or using short slice keys)
        const timestampToken = Date.now().toString().slice(-4);
        const randomHex = Math.floor(100 + Math.random() * 900); // 3 digit random padding
        updateFields.studentId = `INT-${timestampToken}${randomHex}`;
      }
    } else if (status === "Rejected") {
      updateFields.isVerified = false;
    }

    const updatedStudent = await userModel.findByIdAndUpdate(
      studentIdToUpdate,
      { $set: updateFields },
      { new: true }
    ).select("-password");

    if (!updatedStudent) {
      return res.status(404).json({ success: false, message: "Student account context target not found." });
    }

    return res.status(200).json({ 
      success: true, 
      message: `Account has been successfully ${status}`, 
      student: updatedStudent 
    });
  } catch (err) {
    return res.status(500).json({ success: false, message: "Status Engine Mutation Crash: " + err.message });
  }
}